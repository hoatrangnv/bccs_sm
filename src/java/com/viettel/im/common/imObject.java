package com.viettel.im.common;

/**
 *
 * @author tronglv
 */
public class imObject {

    private Object key;
    private Object value;

    public imObject(Object key, Object value) {
        this.key = key;
        this.value = value;
    }

    public imObject() {
    }

    public Object getKey() {
        return key;
    }

    public void setKey(Object key) {
        this.key = key;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    

}
