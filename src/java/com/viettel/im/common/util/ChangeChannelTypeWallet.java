public class ChangeChannelTypeWallet {
    
    private String mobile;
    private String newChannelType;
    private String ewalletId;
    private String newParentId;

    public ChangeChannelTypeWallet() {
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getNewChannelType() {
        return newChannelType;
    }

    public void setNewChannelType(String newChannelType) {
        this.newChannelType = newChannelType;
    }

    public String getEwalletId() {
        return ewalletId;
    }

    public void setEwalletId(String ewalletId) {
        this.ewalletId = ewalletId;
    }

    public String getNewParentId() {
        return newParentId;
    }

    public void setNewParentId(String newParentId) {
        this.newParentId = newParentId;
    }
    
}
