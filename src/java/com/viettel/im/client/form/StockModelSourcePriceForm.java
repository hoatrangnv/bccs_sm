package com.viettel.im.client.form;

import org.apache.struts.action.ActionForm;

/**
 *
 * @author kdvt_phuoctv
 */
public class StockModelSourcePriceForm extends ActionForm {

    private Long stockModelId;
    private String stockModelCode;
    private Long stockTypeId;
    private String name;
    private String sourcePrice;

    private String prevStockModelCode;
    private Long prevStockTypeId;
    private String prevSourcePrice;

    public String getPrevSourcePrice() {
        return prevSourcePrice;
    }

    public void setPrevSourcePrice(String prevSourcePrice) {
        this.prevSourcePrice = prevSourcePrice;
    }

    public String getPrevStockModelCode() {
        return prevStockModelCode;
    }

    public void setPrevStockModelCode(String prevStockModelCode) {
        this.prevStockModelCode = prevStockModelCode;
    }

    public Long getPrevStockTypeId() {
        return prevStockTypeId;
    }

    public void setPrevStockTypeId(Long prevStockTypeId) {
        this.prevStockTypeId = prevStockTypeId;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSourcePrice() {
        return sourcePrice;
    }

    public void setSourcePrice(String sourcePrice) {
        this.sourcePrice = sourcePrice;
    }

    public String getStockModelCode() {
        return stockModelCode;
    }

    public void setStockModelCode(String stockModelCode) {
        this.stockModelCode = stockModelCode;
    }

    public Long getStockModelId() {
        return stockModelId;
    }

    public void setStockModelId(Long stockModelId) {
        this.stockModelId = stockModelId;
    }

    public Long getStockTypeId() {
        return stockTypeId;
    }

    public void setStockTypeId(Long stockTypeId) {
        this.stockTypeId = stockTypeId;
    }
    
    public void resetForm() {
        stockTypeId = null;
        name = null;
        sourcePrice = null;
        stockModelCode = null;
    }
}
