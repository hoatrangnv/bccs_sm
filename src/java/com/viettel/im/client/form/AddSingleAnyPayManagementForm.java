package com.viettel.im.client.form;

import java.util.Date;
/**
 *
 * @author haint
 */
public class AddSingleAnyPayManagementForm implements java.io.Serializable{
    
    private Date fromDate;
    private Date toDate;
    private Long transId;
    private String transType;
    private String staffName;
    private String transDate;
    private Double transAmount;
    private String note;

    public AddSingleAnyPayManagementForm() {
    }

    public AddSingleAnyPayManagementForm(Long transId, String transType, String staffName, String transDate, Double transAmount, String note) {
        this.transId = transId;
        this.transType = transType;
        this.staffName = staffName;
        this.transDate = transDate;
        this.transAmount = transAmount;
        this.note = note;
    }
    
    public Date getFromDate() {
        return fromDate;
    }

    public void setFromDate(Date fromDate) {
        this.fromDate = fromDate;
    }

    public Date getToDate() {
        return toDate;
    }

    public void setToDate(Date toDate) {
        this.toDate = toDate;
    }

    public String getTransType() {
        return transType;
    }

    public void setTransType(String transType) {
        this.transType = transType;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public Double getTransAmount() {
        return transAmount;
    }

    public void setTransAmount(Double transAmount) {
        this.transAmount = transAmount;
    }

    public String getTransDate() {
        return transDate;
    }

    public void setTransDate(String transDate) {
        this.transDate = transDate;
    }

    public Long getTransId() {
        return transId;
    }

    public void setTransId(Long transId) {
        this.transId = transId;
    }
    
}
