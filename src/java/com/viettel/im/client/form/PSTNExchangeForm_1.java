package com.viettel.im.client.form;

import java.util.Date;
import org.apache.struts.action.ActionForm;

/**
 *
 * @author tuannv
 * date 18/02/2009
 *
 */
public class PSTNExchangeForm_1 extends ActionForm {

    private String pstnHeadNumber;
    private String provinceCode;
    private String exchangeCode;
    private Date pstnExchangeDate;

    public PSTNExchangeForm_1() {
       
    }

    /**
     * @return the pstnHeadNumber
     */
    public String getPstnHeadNumber() {
        return pstnHeadNumber;
    }

    /**
     * @param pstnHeadNumber the pstnHeadNumber to set
     */
    public void setPstnHeadNumber(String pstnHeadNumber) {
        this.pstnHeadNumber = pstnHeadNumber;
    }

    /**
     * @return the provinceCode
     */
    public String getProvinceCode() {
        return provinceCode;
    }

    /**
     * @param provinceCode the provinceCode to set
     */
    public void setProvinceCode(String provinceCode) {
        this.provinceCode = provinceCode;
    }

    /**
     * @return the exchangeCode
     */
    public String getExchangeCode() {
        return exchangeCode;
    }

    /**
     * @param exchangeCode the exchangeCode to set
     */
    public void setExchangeCode(String exchangeCode) {
        this.exchangeCode = exchangeCode;
    }

    /**
     * @return the pstnExchangeDate
     */
    public Date getPstnExchangeDate() {
        return pstnExchangeDate;
    }

    /**
     * @param pstnExchangeDate the pstnExchangeDate to set
     */
    public void setPstnExchangeDate(Date pstnExchangeDate) {
        this.pstnExchangeDate = pstnExchangeDate;
    }

   
}
