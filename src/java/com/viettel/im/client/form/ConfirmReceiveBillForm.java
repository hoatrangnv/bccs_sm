package com.viettel.im.client.form;

import org.apache.struts.action.ActionForm;

/**
 *
 * @author TungTV
 * @funtion BillManagement
 * @Date 18/02/2009
 */

public class ConfirmReceiveBillForm extends ActionForm {


    private String[] receivedBill;

    private String billSituation;

    private String fromDateB;

    private String toDateB;
    
    public String[] getReceivedBill() {
        return receivedBill;
    }

    public void setReceivedBill(String[] receivedBill) {
        this.receivedBill = receivedBill;
    }

    public String getBillSituation() {
        return billSituation;
    }

    public void setBillSituation(String billSituation) {
        this.billSituation = billSituation;
    }

    public String getFromDateB() {
        return fromDateB;
    }

    public void setFromDateB(String fromDateB) {
        this.fromDateB = fromDateB;
    }

    public String getToDateB() {
        return toDateB;
    }

    public void setToDateB(String toDateB) {
        this.toDateB = toDateB;
    }

    
}
