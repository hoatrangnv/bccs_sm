package com.viettel.im.client.bean;

/**
 *
 * @author User
 */
public class ViewStaffBean implements java.io.Serializable {

    private Long staffId;
    private String staffCode;
    private String name;
    private String address;
    private String tel;
    private String nameManagement;
    private String nameChannelType;
    private String idNo;
    private String statusName;
    private String shopCode;
    

    public ViewStaffBean() {
    }

    public ViewStaffBean(Long staffId, String staffCode, String name, String address, String tel, String nameManagement, String nameChannelType) {
        this.staffId = staffId;
        this.staffCode = staffCode;
        this.name = name;
        this.address = address;
        this.tel = tel;
        this.nameManagement = nameManagement;
        this.nameChannelType = nameChannelType;
    }

    public String getIdNo() {
        return idNo;
    }

    public void setIdNo(String idNo) {
        this.idNo = idNo;
    }

    public String getStatusName() {
        return statusName;
    }

    public void setStatusName(String statusName) {
        this.statusName = statusName;
    }
    
    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameChannelType() {
        return nameChannelType;
    }

    public void setNameChannelType(String nameChannelType) {
        this.nameChannelType = nameChannelType;
    }

    public String getNameManagement() {
        return nameManagement;
    }

    public void setNameManagement(String nameManagement) {
        this.nameManagement = nameManagement;
    }

    public String getStaffCode() {
        return staffCode;
    }

    public void setStaffCode(String staffCode) {
        this.staffCode = staffCode;
    }

    public Long getStaffId() {
        return staffId;
    }

    public void setStaffId(Long staffId) {
        this.staffId = staffId;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getShopCode() {
        return shopCode;
    }

    public void setShopCode(String shopCode) {
        this.shopCode = shopCode;
    }
    

}
