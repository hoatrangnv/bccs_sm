package com.viettel.im.client.bean;
    
import java.util.Date;

/**
 *
 * @author User
 */
public class ChangeGoodSerialBean {

    public ChangeGoodSerialBean(Long stockModelId, String stockModelName, String typeName, Long TotalQuantity, String SerialX, String SerialN, Date DateChange) {
        this.stockModelId = stockModelId;
        this.stockModelName = stockModelName;
        this.typeName = typeName;
        this.TotalQuantity = TotalQuantity;
        this.SerialX = SerialX;
        this.SerialN = SerialN;
        this.DateChange = DateChange;
    }
    public ChangeGoodSerialBean(){        
    }

    private Long stockModelId;
    private String stockModelName;
    private String typeName;
    private Long TotalQuantity;
    private String SerialX;
    private String SerialN;
    private Date DateChange;

    public Date getDateChange() {
        return DateChange;
    }

    public void setDateChange(Date DateChange) {
        this.DateChange = DateChange;
    }

    public String getSerialN() {
        return SerialN;
    }

    public void setSerialN(String SerialN) {
        this.SerialN = SerialN;
    }

    public String getSerialX() {
        return SerialX;
    }

    public void setSerialX(String SerialX) {
        this.SerialX = SerialX;
    }

    public Long getTotalQuantity() {
        return TotalQuantity;
    }

    public void setTotalQuantity(Long TotalQuantity) {
        this.TotalQuantity = TotalQuantity;
    }

    public Long getStockModelId() {
        return stockModelId;
    }

    public void setStockModelId(Long stockModelId) {
        this.stockModelId = stockModelId;
    }

    public String getStockModelName() {
        return stockModelName;
    }

    public void setStockModelName(String stockModelName) {
        this.stockModelName = stockModelName;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
}
