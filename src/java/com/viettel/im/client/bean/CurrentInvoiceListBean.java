public class CurrentInvoiceListBean {
    private Long invoiceListId;
    private String serialNo;
    private String blockNo;
    private String fromInvoice;
    private String toInvoice;
    private String currInvoiceNo;
    private String invoiceNumber;
    private Long shopId;
    private Long staffId;
    private Long status;

    public CurrentInvoiceListBean() {
    }

    public CurrentInvoiceListBean(Long invoiceListId, String serialNo, String blockNo, String currInvoiceNo) {
        this.invoiceListId = invoiceListId;
        this.serialNo = serialNo;
        this.blockNo = blockNo;
        this.currInvoiceNo = currInvoiceNo;
    }

    
    public CurrentInvoiceListBean(Long invoiceListId, String invoiceNumber) {
        this.invoiceListId = invoiceListId;
        this.invoiceNumber = invoiceNumber;
    }

    public CurrentInvoiceListBean(Long invoiceListId, String serialNo, String blockNo, String fromInvoice, String toInvoice, String currInvoiceNo, String invoiceNumber, Long shopId, Long staffId, Long status) {
        this.invoiceListId = invoiceListId;
        this.serialNo = serialNo;
        this.blockNo = blockNo;
        this.fromInvoice = fromInvoice;
        this.toInvoice = toInvoice;
        this.currInvoiceNo = currInvoiceNo;
        this.invoiceNumber = invoiceNumber;
        this.shopId = shopId;
        this.staffId = staffId;
        this.status = status;
    }

    
    public String getBlockNo() {
        return blockNo;
    }

    public void setBlockNo(String blockNo) {
        this.blockNo = blockNo;
    }

    public String getCurrInvoiceNo() {
        return currInvoiceNo;
    }

    public void setCurrInvoiceNo(String currInvoiceNo) {
        this.currInvoiceNo = currInvoiceNo;
    }

    public String getFromInvoice() {
        return fromInvoice;
    }

    public void setFromInvoice(String fromInvoice) {
        this.fromInvoice = fromInvoice;
    }

    public Long getInvoiceListId() {
        return invoiceListId;
    }

    public void setInvoiceListId(Long invoiceListId) {
        this.invoiceListId = invoiceListId;
    }

    public String getInvoiceNumber() {
        return invoiceNumber;
    }

    public void setInvoiceNumber(String invoiceNumber) {
        this.invoiceNumber = invoiceNumber;
    }

    public String getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(String serialNo) {
        this.serialNo = serialNo;
    }

    public String getToInvoice() {
        return toInvoice;
    }

    public void setToInvoice(String toInvoice) {
        this.toInvoice = toInvoice;
    }

    public Long getShopId() {
        return shopId;
    }

    public void setShopId(Long shopId) {
        this.shopId = shopId;
    }

    public Long getStaffId() {
        return staffId;
    }

    public void setStaffId(Long staffId) {
        this.staffId = staffId;
    }

    public Long getStatus() {
        return status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    

    
}
