package com.viettel.im.client.bean;

/**
 *
 * @author TrongLV
 */
public class ErrorBankReceiptBean {
    
    String date;
    String code;
    String amount;
    String bank;
    String error;
    
    String code_service;
    String code_shop;    
    Long code_service_id;
    Long code_shop_id;

    public ErrorBankReceiptBean() {
    }

    public ErrorBankReceiptBean(String date, String code, String amount, String bank) {
        this.date = date;
        this.code = code;
        this.amount = amount;
        this.bank = bank;
    }   
    

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getBank() {
        return bank;
    }

    public void setBank(String bank) {
        this.bank = bank;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    public String getCode_service() {
        return code_service;
    }

    public void setCode_service(String code_service) {
        this.code_service = code_service;
    }

    public Long getCode_service_id() {
        return code_service_id;
    }

    public void setCode_service_id(Long code_service_id) {
        this.code_service_id = code_service_id;
    }

    public String getCode_shop() {
        return code_shop;
    }

    public void setCode_shop(String code_shop) {
        this.code_shop = code_shop;
    }

    public Long getCode_shop_id() {
        return code_shop_id;
    }

    public void setCode_shop_id(Long code_shop_id) {
        this.code_shop_id = code_shop_id;
    }
    
    
}
