package com.viettel.im.client.bean;

import java.util.Date;

/**
 *
 * @author TungTV
 */
public class ImportHistoryBean implements java.io.Serializable {
    private String stockTypeName;
    private String stockStartIsdn;
    private String stockEndIsdn;
    private Date createdDate;
    private String createdUser;
    private Long totalIsdnNumber;

    public ImportHistoryBean() {
        
    }


    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public String getStockEndIsdn() {
        return stockEndIsdn;
    }

    public void setStockEndIsdn(String stockEndIsdn) {
        this.stockEndIsdn = stockEndIsdn;
    }

    public String getStockStartIsdn() {
        return stockStartIsdn;
    }

    public void setStockStartIsdn(String stockStartIsdn) {
        this.stockStartIsdn = stockStartIsdn;
    }

    public Long getTotalIsdnNumber() {
        return totalIsdnNumber;
    }

    public void setTotalIsdnNumber(Long totalIsdnNumber) {
        this.totalIsdnNumber = totalIsdnNumber;
    }

    public String getStockTypeName() {
        return stockTypeName;
    }

    public void setStockTypeName(String stockTypeName) {
        this.stockTypeName = stockTypeName;
    }

    public String getCreatedUser() {
        return createdUser;
    }

    public void setCreatedUser(String createdUser) {
        this.createdUser = createdUser;
    }

}
