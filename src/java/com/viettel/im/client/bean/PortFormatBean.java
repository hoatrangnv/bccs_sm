package com.viettel.im.client.bean;

/**
 *
 * @author tronglv
 */
public class PortFormatBean {

    // Fields
    private Long id;
    private Long eqId;
    private String eqCode;
    private String eqName;
    private String portFormat;
    private Long status;
    private String type;
    private String typeName;
    private String eqTypeName;

    // Constructors
    /** default constructor */
    public PortFormatBean() {
    }

    public PortFormatBean(Long id, Long eqId, String portFormat, Long status, String type, String eqCode, String eqName, String eqTypeName, String typeName) {
        this.id = id;
        this.eqId = eqId;
        this.eqCode = eqCode;
        this.eqName = eqName;
        this.portFormat = portFormat;
        this.status = status;
        this.type = type;
        this.eqTypeName = eqTypeName;
        this.typeName = typeName;
    }


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    // Property accessors
    public Long getEqId() {
        return this.eqId;
    }

    public void setEqId(Long eqId) {
        this.eqId = eqId;
    }

    public String getPortFormat() {
        return this.portFormat;
    }

    public void setPortFormat(String portFormat) {
        this.portFormat = portFormat;
    }

    public Long getStatus() {
        return this.status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    public String getEqCode() {
        return eqCode;
    }

    public void setEqCode(String eqCode) {
        this.eqCode = eqCode;
    }

    public String getEqName() {
        return eqName;
    }

    public void setEqName(String eqName) {
        this.eqName = eqName;
    }

    public String getEqTypeName() {
        return eqTypeName;
    }

    public void setEqTypeName(String eqTypeName) {
        this.eqTypeName = eqTypeName;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }


}
