package com.viettel.im.client.bean;

/**
 *
 * @author AnDV
 */
public class CommSubscriberBean implements java.io.Serializable {

    private String isdn;
    private String subName;
    private String regTypeCode;
    private String reporttemplate;

    public CommSubscriberBean() {
    }

    public CommSubscriberBean(String isdn, String subName, String regTypeCode, String reporttemplate) {
        this.isdn = isdn;
        this.subName = subName;
        this.regTypeCode = regTypeCode;
        this.reporttemplate = reporttemplate;
    }

    public String getIsdn() {
        return isdn;
    }

    public void setIsdn(String isdn) {
        this.isdn = isdn;
    }

    public String getRegTypeCode() {
        return regTypeCode;
    }

    public void setRegTypeCode(String regTypeCode) {
        this.regTypeCode = regTypeCode;
    }

    public String getSubName() {
        return subName;
    }

    public void setSubName(String subName) {
        this.subName = subName;
    }

    public String getReporttemplate() {
        return reporttemplate;
    }

    public void setReporttemplate(String reporttemplate) {
        this.reporttemplate = reporttemplate;
    }
}
