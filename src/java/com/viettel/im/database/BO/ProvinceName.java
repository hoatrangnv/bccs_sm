public class ProvinceName implements java.io.Serializable{
    private String code;
    private String name;
    private String address;
    private String province;
    private String dslamIp;
    private String brasIp;
    private Long dslamId;
    private Long status;
    private Long productId;
    private Long maxPort;
    private Long usedPort;
    private Long reservedPort;
    private Long x;
    private Long y;
    private Long posId;
    private String prname;

    public ProvinceName() {

    }

    public ProvinceName(Long dslamId) {
        this.dslamId = dslamId;
    }

    public ProvinceName(String code, String name, String address, String province, String dslamIp, String brasIp, Long dslamId, Long status, Long productId, Long maxPort, Long usedPort, Long reservedPort, Long x, Long y, Long posId, String prname) {
        this.code = code;
        this.name = name;
        this.address = address;
        this.province = province;
        this.dslamIp = dslamIp;
        this.brasIp = brasIp;
        this.dslamId = dslamId;
        this.status = status;
        this.productId = productId;
        this.maxPort = maxPort;
        this.usedPort = usedPort;
        this.reservedPort = reservedPort;
        this.x = x;
        this.y = y;
        this.posId = posId;
        this.prname = prname;
    }


    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBrasIp() {
        return brasIp;
    }

    public void setBrasIp(String brasIp) {
        this.brasIp = brasIp;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Long getDslamId() {
        return dslamId;
    }

    public void setDslamId(Long dslamId) {
        this.dslamId = dslamId;
    }

    public String getDslamIp() {
        return dslamIp;
    }

    public void setDslamIp(String dslamIp) {
        this.dslamIp = dslamIp;
    }

    public Long getMaxPort() {
        return maxPort;
    }

    public void setMaxPort(Long maxPort) {
        this.maxPort = maxPort;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPosId() {
        return posId;
    }

    public void setPosId(Long posId) {
        this.posId = posId;
    }

    public String getPrname() {
        return prname;
    }

    public void setPrname(String prname) {
        this.prname = prname;
    }

    public Long getProductId() {
        return productId;
    }

    public void setProductId(Long productId) {
        this.productId = productId;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public Long getReservedPort() {
        return reservedPort;
    }

    public void setReservedPort(Long reservedPort) {
        this.reservedPort = reservedPort;
    }

    public Long getStatus() {
        return status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    public Long getUsedPort() {
        return usedPort;
    }

    public void setUsedPort(Long usedPort) {
        this.usedPort = usedPort;
    }

    public Long getX() {
        return x;
    }

    public void setX(Long x) {
        this.x = x;
    }

    public Long getY() {
        return y;
    }

    public void setY(Long y) {
        this.y = y;
    }


}
