package com.viettel.im.database.BO;

import java.util.Date;

/**
 *
 * @author haint
 */
public class TransAnyPayDetail implements java.io.Serializable {

    private Long transDetailId;
    private Long transId;
    private Date transDate;
    private Long agentId;
    private Double amount;
    private Double openingBalance;
    private Double closingBalance;
    private Long anyPayTransId;
    private String message;
    private String params;

    public TransAnyPayDetail() {
    }

    public Long getAgentId() {
        return agentId;
    }

    public void setAgentId(Long agentId) {
        this.agentId = agentId;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public Double getClosingBalance() {
        return closingBalance;
    }

    public void setClosingBalance(Double closingBalance) {
        this.closingBalance = closingBalance;
    }

    public Double getOpeningBalance() {
        return openingBalance;
    }

    public void setOpeningBalance(Double openingBalance) {
        this.openingBalance = openingBalance;
    }

    public Long getAnyPayTransId() {
        return anyPayTransId;
    }

    public void setAnyPayTransId(Long anyPayTransId) {
        this.anyPayTransId = anyPayTransId;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getParams() {
        return params;
    }

    public void setParams(String params) {
        this.params = params;
    }

    public Date getTransDate() {
        return transDate;
    }

    public void setTransDate(Date transDate) {
        this.transDate = transDate;
    }

    public Long getTransDetailId() {
        return transDetailId;
    }

    public void setTransDetailId(Long transDetailId) {
        this.transDetailId = transDetailId;
    }

    public Long getTransId() {
        return transId;
    }

    public void setTransId(Long transId) {
        this.transId = transId;
    }
}
