package com.viettel.im.database.BO;

import com.viettel.database.BO.BasicBO;

/**
 * Exchange entity.
 *
 * @author MyEclipse Persistence Tools
 */
public class Exchange extends BasicBO {

    // Fields
    private Long exchangeId;
    private Long typeId;
    private String name;
    private String description;
    private Long status;
    private Long maxGroup;
    private Long serverId;

    // Constructors
    /** default constructor */
    public Exchange() {
    }

    /** minimal constructor */
    public Exchange(Long exchangeId, Long typeId, String name) {
        this.exchangeId = exchangeId;
        this.typeId = typeId;
        this.name = name;
    }

    /** full constructor */
    public Exchange(Long exchangeId, Long typeId, String name,
            String description, Long status, Long maxGroup, Long serverId) {
        this.exchangeId = exchangeId;
        this.typeId = typeId;
        this.name = name;
        this.description = description;
        this.status = status;
        this.maxGroup = maxGroup;
        this.serverId = serverId;
    }

    // Property accessors
    public Long getExchangeId() {
        return this.exchangeId;
    }

    public void setExchangeId(Long exchangeId) {
        this.exchangeId = exchangeId;
    }

    public Long getTypeId() {
        return this.typeId;
    }

    public void setTypeId(Long typeId) {
        this.typeId = typeId;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return this.description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Long getStatus() {
        return this.status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    public Long getMaxGroup() {
        return this.maxGroup;
    }

    public void setMaxGroup(Long maxGroup) {
        this.maxGroup = maxGroup;
    }

    public Long getServerId() {
        return this.serverId;
    }

    public void setServerId(Long serverId) {
        this.serverId = serverId;
    }
}
