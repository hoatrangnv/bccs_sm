package com.viettel.im.database.BO;

/**
 * Card entity.
 * 
 * @author MyEclipse Persistence Tools
 */

public class Card implements java.io.Serializable {

	// Fields

	private Long cardId;
	private String code;
	private Long equipmentId;
	private String name;
	private String cardType;
	private Long totalPort;
	private Long status;
	private String description;

	// Constructors

	/** default constructor */
	public Card() {
	}

	/** minimal constructor */
	public Card(Long cardId, Long equipmentId) {
		this.cardId = cardId;
		this.equipmentId = equipmentId;
	}

	/** full constructor */
	public Card(Long cardId, String code, Long equipmentId, String name,
			String cardType, Long totalPort, Long status, String description) {
		this.cardId = cardId;
		this.code = code;
		this.equipmentId = equipmentId;
		this.name = name;
		this.cardType = cardType;
		this.totalPort = totalPort;
		this.status = status;
		this.description = description;
	}

	// Property accessors

	public Long getCardId() {
		return this.cardId;
	}

	public void setCardId(Long cardId) {
		this.cardId = cardId;
	}

	public String getCode() {
		return this.code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Long getEquipmentId() {
		return this.equipmentId;
	}

	public void setEquipmentId(Long equipmentId) {
		this.equipmentId = equipmentId;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCardType() {
		return this.cardType;
	}

	public void setCardType(String cardType) {
		this.cardType = cardType;
	}

	public Long getTotalPort() {
		return this.totalPort;
	}

	public void setTotalPort(Long totalPort) {
		this.totalPort = totalPort;
	}

	public Long getStatus() {
		return this.status;
	}

	public void setStatus(Long status) {
		this.status = status;
	}

	public String getDescription() {
		return this.description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

}