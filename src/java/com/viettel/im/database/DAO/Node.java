package com.viettel.im.database.DAO;

/**
 *
 * @author thanhnv
 */
public class Node {

    private String title;
    private String isFolder;
    private String objectId;
    private String link;
    private String widgetId;
    private String key;
    private String name;
    private boolean isCheck;

    public Node(String key, String name) {
        this.key = key;
        this.name = name;
    }

    
    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWidgetId() {
        return widgetId;
    }

    public void setWidgetId(String widgetId) {
        this.widgetId = widgetId;
    }
    

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
    

    public Node(String title, String isFolder, String objectId,String link) {

        this.title = title;
        this.isFolder = isFolder;
        this.objectId = objectId;
        this.link = link;
        this.widgetId = objectId;
    }
    public Node(String title, String isFolder, String objectId,String link, boolean checkValue) {

        this.title = title;
        this.isFolder = isFolder;
        this.objectId = objectId;
        this.link = link;
        this.widgetId = objectId;
        this.isCheck = checkValue;
    }

    public String getIsFolder() {
        return isFolder;
    }

    public void setIsFolder(String isFolder) {
        this.isFolder = isFolder;
    }

    public String getObjectId() {
        return objectId;
    }

    public void setObjectId(String objectId) {
        this.objectId = objectId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isIsCheck() {
        return isCheck;
    }

    public void setIsCheck(boolean isCheck) {
        this.isCheck = isCheck;
    }
  
}
